class Section():
    def __init__(self):
        self.locations = []


    def __repr__(self):
        return str(self.locations)


    def add_location(self, x, y):
        self.locations.extend([(x, y)])

    def find_unique(self):
        return list(set(self.locations))


class Grid():
    def __init__(self, data):
        self.map = self.grid = [[s for s in r] for r in data.split('\n')]
        self.checked = []
        self.sections = []
        self._height = len(self.map)-1
        self._width = len(self.map[0])-1

    def __repr__(self):
        v = ''
        for c in self.grid:
            for r in c:
                v += r
            v += '\n'
        return v

    
    def _valid_move(self, x, y, value, dct):
        dx = dct[0]
        dy = dct[1]
        if x == 0 and dx == -1:
            return False
        if y == 0 and dy == -1:
            return False
        if x == self._width and dx == 1:
            return False
        if y == self._height and dy == 1:
            return False
        # if (x+dx, y+dy) in self.checked:
        #     return False 

        return self.map[y + dy][x + dx] == self.map[y][x]
        

    def find_section_perimeter(self, x, y, section):
        if (x, y) in self.checked:
            return
        else:
            for move in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
                dx = move[0]
                dy = move[1]
                value = self.map[y][x]
                if self._valid_move(x, y, value, move):
                    self.checked.append((x, y))
                    section.add_location(x, y)
                    if (x+dx, y+dy) not in self.checked:
                        self.find_section_perimeter(x+dx, y+dy, section)
                    

        
