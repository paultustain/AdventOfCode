def borders(location, section):
    default = 4
    for move in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
        if (location[0] + move[0], location[1] + move[1]) in section:
            default -= 1 
    return default


def find_perimeter(section):
    perim = 0
    for l in section:
        perim += borders(l, section)

    return perim

